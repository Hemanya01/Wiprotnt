package miniproject;

public class MiniProject3Card 
{
	private char symbol;
    private int number;
    public MiniProject3Card(char symbol, int number) 
    {
        this.symbol = symbol;
        this.number = number;
    }
    public char getSymbol() 
    {
        return symbol;
    }
    public int getNumber() 
    {
        return number;
    }
}
